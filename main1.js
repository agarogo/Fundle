$('.open-opup').click(function(e) {
    e.preventDefault();
    $('.opup-bg').fadeIn(800);
    $('html').addClass('no-scroll');
});

$('.close-opup').click(function() {
    $('.opup-bg').fadeOut(800);
    $('html').removeClass('no-scroll');
});






